public class UneryOperator {
    public static void main(String[] args) {
        int x = 10;
        int y;
        y = +x;
        System.out.println("Result is "+ y);
        
        y = -x;
        System.out.println("Result is "+ y);

        int a = -10;
        int b;
        b = +a;
        System.out.println("Result is "+ b);
        
        b = -a;
        System.out.println("Result is "+ b);
    }
    
}
